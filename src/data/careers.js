export function getAIRecommendation(scores) {
    const threshold = 60;
    const sortedScores = Object.entries(scores)
        .filter(([, score]) => score >= threshold)
        .sort(([, a], [, b]) => b - a);

    if (sortedScores.length === 0) {
        return {
            message: "Hozircha natijalaringiz biroz past. Ko'proq bilim olishga va o'zingiz qiziqqan fanni chuqur o'rganishga harakat qiling!",
            careers: [
                { title: 'O\'z ustida ishlash', icon: '📚', desc: 'Har bir soha bilim bilan boshlanadi.', salary: 'Kelajak sarmoyasi', match: 100 },
            ],
            topSubject: null,
        };
    }

    const strongSubjectIds = sortedScores.map(([id]) => id);
    const recommendations = [];

    // 1. Avval kombinatsiyalarni tekshiramiz (eng aniq tavsiyalar)
    combinations.forEach(combo => {
        const hasAll = combo.subjects.every(s => strongSubjectIds.includes(s));
        if (hasAll) {
            recommendations.push(combo.career);
        }
    });

    // 2. Keyin har bir kuchli fan uchun odatiy kasblarni qo'shamiz
    strongSubjectIds.slice(0, 3).forEach(subId => {
        const subCareers = careerMap[subId]?.careers || [];
        subCareers.forEach(c => {
            if (!recommendations.find(r => r.title === c.title)) {
                recommendations.push(c);
            }
        });
    });

    const subjectNames = {
        math: 'Matematika', physics: 'Fizika', chemistry: 'Kimyo',
        biology: 'Biologiya', history: 'Tarix', english: 'Ingliz tili',
        literature: 'Adabiyot', informatics: 'Informatika', uzbek: "Ona tili",
        russian: 'Rus tili'
    };

    const strongNames = strongSubjectIds.slice(0, 3).map(id => subjectNames[id]).join(', ');
    const topSubject = strongSubjectIds[0];

    return {
        message: `Siz asosan ${strongNames} fanlari bo'yicha yuqori natija ko'rsatdingiz. AI tahlillariga ko'ra, sizga quyidagi zamonaviy kasblar mos keladi:`,
        careers: recommendations.slice(0, 6), // Eng yaxshi 6 tasi
        topSubject,
    };
}
